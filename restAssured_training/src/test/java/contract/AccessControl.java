package contract;

public class AccessControl {
	private String autoPlay;
	private String comment;
	private String commentVote;
	private String embed;
	private String list;
	private String rate;
	private String syndicate;
	private String videoRespond;

	public String getAutoPlay() {
		return autoPlay;
	}

	public void setAutoPlay(String value) {
		autoPlay = value;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String value) {
		comment = value;
	}

	public String getCommentVote() {
		return commentVote;
	}

	public void setCommentVote(String value) {
		commentVote = value;
	}

	public String getEmbed() {
		return embed;
	}

	public void setEmbed(String value) {
		embed = value;
	}

	public String getList() {
		return list;
	}

	public void setList(String value) {
		list = value;
	}

	public String getRate() {
		return rate;
	}

	public void setRate(String value) {
		rate = value;
	}

	public String getSyndicate() {
		return syndicate;
	}

	public void setSyndicate(String value) {
		syndicate = value;
	}

	public String getVideoRespond() {
		return videoRespond;
	}

	public void setVideoRespond(String value) {
		videoRespond = value;
	}
}
